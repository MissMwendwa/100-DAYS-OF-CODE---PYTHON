# Area, Perimeter and Volume of Rectangle

# We prompt the user to give the measurements

# Measurements
print(" kindly fill the information")
print("Length")
len = int(input())
print("Width")
wid = int(input())
print(" Height")
hgt = int(input())

# Perform the Perimeter
per = 2*(len + wid)
print(per)

# Perform Area
area = len * wid
print(area)

# Perform Volume
vol = len * wid * hgt
print(vol)